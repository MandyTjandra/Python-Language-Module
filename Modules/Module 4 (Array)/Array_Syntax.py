angka = [1, 2, 3, 4, 5]
print(angka) # Original array

angka.append(6) # Add element to the array
print(angka) # Print the new array

angka.pop(2) # Remove an element on 2nd index
print(angka) # Print new array

angka.remove(1) # Similar to pop but remove the element not index
print(angka) # Print the new array 

# Other array syntaxes:
# append()	    Adds an element at the end of the list
# clear()	    Removes all the elements from the list
# copy()	    Returns a copy of the list
# count()	    Returns the number of elements with the specified value
# extend()	    Add the elements of a list (or any iterable), to the end of the current list
# index()	    Returns the index of the first element with the specified value
# insert()	    Adds an element at the specified position
# pop()	        Removes the element at the specified position
# remove()	    Removes the first item with the specified value
# reverse()	    Reverses the order of the list
# sort()	    Sorts the list
